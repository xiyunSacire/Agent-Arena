from typing import Callable
from HBPU_Student_Client.models import Task


def create_simple_agent(template: str = "default") -> Callable:
    """
    创建一个基于规则的简单智能体处理器（工厂函数）。

    该函数采用工厂模式，根据传入的模板配置返回一个具体的任务处理闭包。
    它演示了如何根据任务内容（Task Content）进行关键词匹配，并返回符合
    ArenaClient 协议标准的字典结构。

    处理逻辑说明：
    1. 文本分析类：检测到 "分析" 或 "总结" 关键词时，返回模拟的分析结果。
    2. 数学计算类：检测到 "计算" 或 "数学" 关键词时，返回模拟的计算结果。
    3. 通用兜底类：未匹配到特定关键词时，返回通用回答。

    Args:
        template (str): 处理器模板名称。默认为 "default"。
                        目前仅支持默认模板，预留用于未来扩展不同策略的 Agent。

    Returns:
        Callable: 一个异步或同步的任务处理函数 (default_agent)，该函数接收
                  Task 对象并返回包含 reasoning 和 final_answer 的字典。

    返回数据结构示例:
        {
            "reasoning": "执行数学计算",      # 思维链/推理过程，用于前端展示
            "final_answer": "计算结果为: 42", # 最终答案，用于评分
            "tool_used": "规则引擎"          # 标记使用的工具，用于审计
        }
    """
    def default_agent(task: Task) -> dict:
        print(f"\n📝 任务内容: {task.content[:100]}...")
        # 规则匹配逻辑
        if "分析" in task.content or "总结" in task.content:
            reasoning = "根据任务要求进行文本分析"
            final_answer = "这是一段示例分析结果..."
        elif "计算" in task.content or "数学" in task.content:
            reasoning = "执行数学计算"
            final_answer = "计算结果为: 42"
        else:
            reasoning = "通用任务处理"
            final_answer = "这是我的回答"

        return {
            "reasoning": reasoning,
            "final_answer": final_answer,
            "tool_used": "规则引擎",
        }

    return default_agent